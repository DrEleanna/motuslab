let currentLang = "el";

function applyTranslations() {
  const dictionary = translations[currentLang];
  document.documentElement.lang = currentLang;
  document.documentElement.dataset.lang = currentLang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dictionary[key]) el.textContent = dictionary[key];
  });
  document.querySelectorAll(".lang-toggle span").forEach((span) => {
    span.classList.toggle("lang-active", span.textContent.toLowerCase() === currentLang);
  });
  renderServices();
  renderBlog();
}

function renderServices() {
  const grid = document.getElementById("servicesGrid");
  grid.innerHTML = services.map((service) => {
    const item = service[currentLang];
    return `
      <article class="service-card">
        <div class="service-image"><img src="${service.image}" alt="${item.title}"></div>
        <div class="service-body">
          <h3>${item.title}</h3>
          <p>${item.text}</p>
        </div>
      </article>
    `;
  }).join("");
}

function renderBlog() {
  const grid = document.getElementById("blogGrid");
  if (!grid) return;
  grid.innerHTML = blogPosts.map((post) => {
    const item = post[currentLang];
    const type = post.type[currentLang] || post.type.en;
    const media = post.embed
      ? `<div class="blog-media"><iframe src="${post.embed}" title="${item.title}" loading="lazy" allowfullscreen></iframe></div>`
      : `<div class="blog-media"><img src="${post.image}" alt="${item.title}"></div>`;
    return `
      <article class="blog-card">
        ${media}
        <div class="blog-body">
          <span class="blog-type">${type}</span>
          <h3>${item.title}</h3>
          <p>${item.text}</p>
          <a class="blog-link" href="${post.link}">${currentLang === "el" ? "Άνοιγμα" : "Open"}</a>
        </div>
      </article>
    `;
  }).join("");
}

document.getElementById("langToggle").addEventListener("click", () => {
  currentLang = currentLang === "el" ? "en" : "el";
  applyTranslations();
});

document.getElementById("year").textContent = new Date().getFullYear();
applyTranslations();
